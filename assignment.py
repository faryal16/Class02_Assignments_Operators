print("\tAssignment Operators")
print("Assignment Operators are those operators who assign values to variables. ")

x = 100  # Starting value

print("\nAssign value =")
print(f" x = {x}")

print("\nAdd and assign +=")
prev_x = x
x += 3
print(f" {prev_x} += 3 = {x}")

print("\nSubtract and assign -=")
prev_x = x
x -= 4
print(f" {prev_x} -= 4 = {x}")

print("\nMultiply and assign *=")
prev_x = x
x *= 2
print(f" {prev_x} *= 2 = {x}")

print("\nDivide and assign /=")
prev_x = x
x /= 8
print(f" {prev_x} /= 8 = {x}")

print("\nFloor divide and assign //=")
prev_x = x
x //= 5
print(f" {prev_x} //= 5 = {x}")

print("\nModulus and assign %=")
prev_x = x
x %= 7
print(f" {prev_x} %= 7 = {x}")

print("\nExponentiate and assign **=")
prev_x = x
x **= 2
print(f" {prev_x} **= 2 = {x}")
