print("\tComparison Operators")
print("Comparison Operators are those operators who  compare values and return True or False. ")

a= 10
b= 3
equal= a==b
print("\nEqual to ==")
print(f"{a} == {b}  \n {equal}")

not_equal =  a!=b
print("\nNot equal to !=")
print(f"{a} != {b}  \n {not_equal}")

grater = a > b
print("\nGrater than >")
print(f"{a} > {b} \n {grater}")

less = a < b
print("\nLess than <")
print(f"{a} < {b} \n {less}")

grater_equal = a >= b
print("\nGrater than or equal to >=")
print(f"{a} >= {b} \n {grater_equal}")

less_equal = a <= b
print("\nLess than or equal to <=")
print(f"{a} <= {b} \n {less_equal}")